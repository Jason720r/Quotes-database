class Product():

    def __init__(self, id, title, image, price, deliveryTime, inStock, stockQuantity, typeId):
        self.id = id
        self.title = title
        self.image = image
        self.price = price
        self.deliveryTime = deliveryTime
        self.inStock = inStock
        self.stockQuantity = stockQuantity
        self.typeId = typeId